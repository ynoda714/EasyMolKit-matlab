function recipe(name)
% recipe  Display the installation recipe for a supported library.
%
%   emk.setup.recipe(name)
%
%   Prints the Track classification, installation commands, license, and
%   any important notes for the named library to the MATLAB Command Window.
%
%   Track 1 libraries (installed into Embedded Python via installExtra):
%     "pubchempy"       - PubChem REST API client
%     "mordred"         - 1800+ molecular descriptors
%     "biopython"       - PDB / sequence analysis
%     "torch"           - PyTorch CPU-only (~800 MB; required before torch_geometric/transformers)
%     "torch_geometric" - PyG graph neural networks (requires torch)
%     "transformers"    - HuggingFace Transformers (requires torch)
%     "datasets"        - HuggingFace Datasets (pairs with transformers)
%     "meeko"           - Ligand PDBQT preparation for AutoDock Vina (LGPL-2.1)
%     "vina"            - AutoDock Vina docking engine Python API (Apache-2.0)
%     "pdbfixer"        - PDB structure preparation; auto-installs openmm (MIT)
%     "docking"         - Combined recipe: meeko + vina + pdbfixer pipeline
%
%   Track 2 libraries (require external CPython; use useExternal to connect):
%     "openbabel"   - Chemical format conversion and 3D coordinate generation
%     "mdanalysis"  - Molecular dynamics trajectory analysis
%     "pymol"       - 3D molecular visualization
%
%   Error IDs:
%     emk:setup:recipe:unknownLibrary - unsupported name
%
%   See also: emk.setup.installExtra, emk.setup.useExternal, emk.setup.validate

    arguments
        name (1,1) string
    end

    switch name
        case "pubchempy"
            printTrack1_( ...
                "pubchempy", ...
                "PubChem REST API client -- compound search and property lookup", ...
                "pubchempy", ...
                "MIT", ...
                "Pure Python. No additional system dependencies.");

        case "mordred"
            printTrack1_( ...
                "mordred", ...
                "1800+ molecular descriptors for QSAR model building", ...
                "mordred", ...
                "BSD-3 (mordredcommunity fork)", ...
                "Installs the mordredcommunity package. Requires numpy and scipy.");

        case "biopython"
            printTrack1_( ...
                "biopython", ...
                "PDB / FASTA / GenBank parsing and bioinformatics utilities", ...
                "biopython", ...
                "Biopython License (BSD-like; see https://biopython.org/DIST/LICENSE)", ...
                "Verify the full license text before use in commercial projects.");

        case "meeko"
            printTrack1_( ...
                "meeko", ...
                "Ligand PDBQT preparation for AutoDock Vina docking", ...
                "meeko", ...
                "LGPL-2.1", ...
                ["Python dynamic import does not trigger LGPL copyleft for EasyMolKit (MIT).", ...
                 "See docs/compliance.md CL-7 for full usage rationale."]);

        case "vina"
            printVina_();

        case "pdbfixer"
            printPdbfixer_();

        case "docking"
            printDocking_();

        case "torch"
            printTrack1_( ...
                "torch", ...
                "PyTorch deep learning framework (CPU-only build, ~800 MB)", ...
                "torch --index-url https://download.pytorch.org/whl/cpu", ...
                "BSD-3", ...
                ["CPU-only wheel. GPU/CUDA support is not available in Embedded Python.", ...
                 "Must be installed BEFORE torch_geometric or transformers.", ...
                 "Large download (~800 MB). Ensure stable internet connection."]);

        case "torch_geometric"
            printTrack1_( ...
                "torch_geometric", ...
                "PyTorch Geometric -- graph neural networks for molecules", ...
                "torch_geometric", ...
                "MIT", ...
                ["Requires torch to be installed first: emk.setup.installExtra(""torch"")", ...
                 "Also installs companion packages: torch_scatter, torch_sparse, torch_cluster.", ...
                 "Version URLs are auto-detected from the installed torch version."]);

        case "transformers"
            printTrack1_( ...
                "transformers", ...
                "HuggingFace Transformers -- pre-trained language and chemistry models", ...
                "transformers", ...
                "Apache-2.0", ...
                ["Requires torch to be installed first: emk.setup.installExtra(""torch"")", ...
                 "Compatible with MIT license. No additional restrictions."]);

        case "datasets"
            printTrack1_( ...
                "datasets", ...
                "HuggingFace Datasets -- fast dataset loading and processing", ...
                "datasets", ...
                "Apache-2.0", ...
                ["Commonly used together with transformers for model training and evaluation.", ...
                 "Compatible with MIT license. No additional restrictions."]);

        case "openbabel"
            printTrack2_( ...
                "openbabel", ...
                "Chemical format conversion (110+ formats) and 3D coordinate generation", ...
                ["REQUIREMENT: Python 3.8 or earlier on Windows.", ...
                 "  The official Windows installer (Open Babel 3.1.1, released May 2020)", ...
                 "  was compiled against Python <= 3.8.  Python 3.9+ is NOT supported.", ...
                 "  If you only have Python 3.9+, consider RDKit (already in Track 1)", ...
                 "  for format conversion, or use WSL/Linux where apt-get install works.", ...
                 "", ...
                 "IMPORTANT: Do NOT run 'pip install openbabel'.", ...
                 "           pip tries to build from source (needs SWIG + C++ headers).", ...
                 "           The MSI installer places Python bindings directly.", ...
                 "", ...
                 "Step 1 -- Install Python 3.8 from python.org:", ...
                 "         https://www.python.org/downloads/release/python-3817/", ...
                 "", ...
                 "Step 2 -- Download the Windows installer (with Python bindings):", ...
                 "         https://github.com/openbabel/openbabel/releases/tag/openbabel-3-1-1", ...
                 "         Choose: OpenBabel-3.1.1-x64.exe (36.9 MB)", ...
                 "         During installation: check the 'Python bindings' checkbox.", ...
                 "         When asked, select Python 3.8 (e.g., C:\Python38).", ...
                 "", ...
                 "Step 3 -- Verify the installation from cmd/PowerShell:", ...
                 "         C:\Python38\python.exe -c ""import openbabel; print('OK')""", ...
                 "", ...
                 "Step 4 -- Connect EasyMolKit to that Python:", ...
                 "         emk.setup.useExternal(""C:\\Python38\\python.exe"")", ...
                 "         NOTE: No venv needed. Point directly to the Python the MSI installed into.", ...
                 "", ...
                 "Step 5 -- Validate:", ...
                 "         T = emk.setup.validate(Libraries=""openbabel"")"], ...
                "GPLv2", ...
                ["WARNING: Open Babel is licensed under GPLv2.", ...
                 "EasyMolKit (MIT) is not affected, but your scripts", ...
                 "that use Open Babel may be subject to GPLv2 terms.", ...
                 "For automated installation (pip-only), consider:", ...
                 "  emk.setup.installTrack2(""mdanalysis"")  % or ""pymol"""]);

        case "mdanalysis"
            printTrack2_( ...
                "mdanalysis", ...
                "Molecular dynamics trajectory analysis (GROMACS, AMBER, NAMD, ...)", ...
                ["Step 1 -- Create a CPython 3.10+ virtual environment:", ...
                 "         python -m venv C:\envs\mdenv", ...
                 "Step 2 -- Install MDAnalysis into it:", ...
                 "         C:\envs\mdenv\Scripts\pip install MDAnalysis", ...
                 "Step 3 -- Connect EasyMolKit:", ...
                 "         emk.setup.useExternal(""C:\\envs\\mdenv\\python.exe"")", ...
                 "Step 4 -- Validate:", ...
                 "         T = emk.setup.validate(Libraries=""mdanalysis"")"], ...
                "GPLv2+", ...
                ["WARNING: GPL library.", ...
                 "EasyMolKit (MIT) is unaffected, but your scripts that", ...
                 "use MDAnalysis may be subject to GPLv2+ terms."]);

        case "pymol"
            printTrack2_( ...
                "pymol", ...
                "3D molecular visualization -- PyMOL Open-Source edition", ...
                ["RECOMMENDED: Use installTrack2 for automatic setup:", ...
                 "         emk.setup.installTrack2(""pymol"", BasePython=""C:\\Python310\\python.exe"")", ...
                 "", ...
                 "MANUAL STEPS (if installTrack2 is not used):", ...
                 "  NOTE: pymol-open-source on PyPI causes DLL load failures on Windows.", ...
                 "        Use Gohlke prebuilt wheels which bundle all native DLLs:", ...
                 "        https://github.com/cgohlke/pymol-open-source-wheels/releases", ...
                 "", ...
                 "Step 1 -- Create a CPython 3.10+ virtual environment:", ...
                 "         python -m venv C:\envs\pymolenv", ...
                 "Step 2 -- Install PyMOL from Gohlke prebuilt wheel (cp310 example):", ...
                 "         C:\envs\pymolenv\Scripts\pip install ^", ...
                 "           https://github.com/cgohlke/pymol-open-source-wheels/releases/download/v2025.2.2/pymol-3.1.0-cp310-cp310-win_amd64.whl", ...
                 "         (For cp311/cp312/cp313 see the releases page above)", ...
                 "Step 3 -- Connect EasyMolKit:", ...
                 "         emk.setup.useExternal(""C:\\envs\\pymolenv\\Scripts\\python.exe"")", ...
                 "Step 4 -- Validate:", ...
                 "         T = emk.setup.validate(Libraries=""pymol"")"], ...
                "Python-2.0 (PSF) / BSD hybrid", ...
                ["Confirm license at:", ...
                 "  https://github.com/schrodinger/pymol-open-source/blob/master/LICENSE"]);

        otherwise
            error("emk:setup:recipe:unknownLibrary", ...
                "Unknown library: '%s'.\n" + ...
                "Supported: pubchempy, mordred, biopython, " + ...
                "torch, torch_geometric, transformers, datasets, " + ...
                "meeko, vina, pdbfixer, docking, " + ...
                "openbabel, mdanalysis, pymol.", name);
    end
end

