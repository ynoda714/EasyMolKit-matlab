function printDocking_()
% Display combined Track 1 docking pipeline recipe (meeko + vina + pdbfixer).
    sep = repmat("=", 1, 62);
    logInfo("%s", sep);
    logInfo("Recipe: docking pipeline  (Track 1 -- Embedded Python)");
    logInfo("%s", sep);
    logInfo("Description  : AutoDock Vina docking pipeline");
    logInfo("               Components: meeko + vina + pdbfixer (+ openmm)");
    if emk.util.isOnline()
        logInfo("Install via  : !~/.local/bin/pip install --user meeko vina pdbfixer");
        logInfo("               (MATLAB Online: installExtra() is not available)", "");
    else
        logInfo("Install via  : emk.setup.installExtra(""meeko"")");
        logInfo("               emk.setup.installExtra(""pdbfixer"")");
        logInfo("               (pdbfixer auto-installs openmm >= 8.2 as dependency)");
        logInfo("               --- vina: No Windows wheel. Use standalone binary ---");
        logInfo("               New-Item -ItemType Directory -Force ""python_env_t2\\vina""");
        logInfo("               Invoke-WebRequest ``");
        logInfo("                 -Uri ""https://github.com/ccsb-scripps/AutoDock-Vina/releases/download/v1.2.7/vina_1.2.7_win.exe"" ``");
        logInfo("                 -OutFile ""python_env_t2\\vina\\vina.exe""");
        logInfo("               For full details: emk.setup.recipe(""vina"")");
    end
    logInfo("License      : meeko: LGPL-2.1  /  vina: Apache-2.0");
    logInfo("               pdbfixer: MIT  /  openmm: MIT + LGPL");
    logInfo("Sample data  : data/docking/sample_ligand.sdf");
    logInfo("               data/docking/sample_protein.pdb");
    logInfo("Notes        : meeko LGPL dynamic import is safe for MIT projects.");
    logInfo("               openmm LGPL dynamic import is safe for MIT projects.");
    logInfo("               See docs/compliance.md for full usage rationale.");
    logInfo("%s", sep);
end

% -------------------------------------------------------------------------
function printTrack1_(name, description, installName, license_, notes)
% Display a Track 1 (Embedded Python / installExtra) recipe.
% On MATLAB Online, show the pip --user alternative instead.
    sep = repmat("=", 1, 62);
    logInfo("%s", sep);
    logInfo("Recipe: %s  (Track 1 -- Embedded Python)", name);
    logInfo("%s", sep);
    logInfo("Description  : %s", description);
    if emk.util.isOnline()
        logInfo("Install via  : !~/.local/bin/pip install --user %s", installName);
        logInfo("               (MATLAB Online: installExtra() is not available)", "");
    else
        logInfo("Install via  : emk.setup.installExtra(""%s"")", installName);
    end
    logInfo("License      : %s", license_);
    logInfo("Notes        : %s", notes);
    logInfo("%s", sep);
end

% -------------------------------------------------------------------------
function printTrack2_(name, description, steps, license_, notes)
% Display a Track 2 (external CPython) recipe.
% On MATLAB Online, Track 2 libraries cannot be installed (persistent venv
% creation is not supported).  Show a brief unavailability notice instead
% of Windows-specific paths that are meaningless in the Online environment.
    sep = repmat("=", 1, 62);
    logInfo("%s", sep);
    logInfo("Recipe: %s  (Track 2 -- External Python required)", name);
    logInfo("%s", sep);
    logInfo("Description  : %s", description);
    if emk.util.isOnline()
        logInfo("Availability : NOT available on MATLAB Online.", "");
        logInfo("               Track 2 libraries require a Windows Desktop", "");
        logInfo("               environment with a persistent CPython + venv.", "");
    else
        logInfo("Installation :");
        for i = 1:numel(steps)
            logInfo("  %s", steps(i));
        end
    end
    logInfo("License      : %s", license_);
    for i = 1:numel(notes)
        if i == 1
            logInfo("Notes        : %s", notes(i));
        else
            logInfo("               %s", notes(i));
        end
    end
    logInfo("%s", sep);
end

% -------------------------------------------------------------------------
function printVina_()
% Display the AutoDock Vina recipe.
% On Windows, the PyPI vina package has no pre-built wheel, so show the
% standalone binary installation steps (Track 2 style).
% On Linux/macOS/WSL2, installExtra() is available.
    sep = repmat("=", 1, 62);
    logInfo("%s", sep);
    if emk.util.isOnline()
        logInfo("Recipe: vina  (MATLAB Online -- pip --user)");
        logInfo("%s", sep);
        logInfo("Description  : AutoDock Vina molecular docking engine Python API");
        logInfo("Install via  : !~/.local/bin/pip install --user vina");
        logInfo("License      : Apache-2.0");
    elseif ispc()
        logInfo("Recipe: vina  (Windows -- Standalone Binary, Track 2 style)");
        logInfo("%s", sep);
        logInfo("Description  : AutoDock Vina molecular docking engine");
        logInfo("               Note: PyPI has NO Windows wheel -- standalone exe required.");
        logInfo("Installation :");
        logInfo("  Step 1 -- Create the binary folder (run in PowerShell):");
        logInfo("            New-Item -ItemType Directory -Force ""python_env_t2\\vina""");
        logInfo("  ");
        logInfo("  Step 2 -- Download the Windows binary (~16 MB):");
        logInfo("            Invoke-WebRequest ``");
        logInfo("              -Uri ""https://github.com/ccsb-scripps/AutoDock-Vina/releases/download/v1.2.7/vina_1.2.7_win.exe"" ``");
        logInfo("              -OutFile ""python_env_t2\\vina\\vina.exe""");
        logInfo("            (Or download manually from the Releases page and save as vina.exe)");
        logInfo("            https://github.com/ccsb-scripps/AutoDock-Vina/releases");
        logInfo("  ");
        logInfo("  Step 3 -- Verify from PowerShell:");
        logInfo("            .\\python_env_t2\\vina\\vina.exe --help");
        logInfo("  ");
        logInfo("  Step 4 -- Call from MATLAB:");
        logInfo("            vinaExe = fullfile(pwd, ""python_env_t2"", ""vina"", ""vina.exe"");");
        logInfo("            system(['""' vinaExe '"" --help'])");
        logInfo("License      : Apache-2.0");
        logInfo("Notes        : No Python venv needed. The standalone exe is equivalent");
        logInfo("               to the pip ""vina"" package on Linux/macOS.");
        logInfo("               For pip-based usage, run on Linux/macOS/WSL2 where");
        logInfo("               emk.setup.installExtra(""vina"") works normally.");
    else
        % Linux / macOS -- installExtra is available
        logInfo("Recipe: vina  (Track 1 -- Embedded Python)");
        logInfo("%s", sep);
        logInfo("Description  : AutoDock Vina molecular docking engine Python API");
        logInfo("Install via  : emk.setup.installExtra(""vina"")");
        logInfo("License      : Apache-2.0");
        logInfo("Notes        : Compatible with MIT license. No additional restrictions.");
    end
    logInfo("%s", sep);
end

% -------------------------------------------------------------------------
function printPdbfixer_()
% Display the pdbfixer recipe with platform-specific instructions.
% Windows Desktop: openmm .pyd is blocked by Smart App Control -- direct to Online.
% MATLAB Online (Linux): pip --user works; show both flag and manual paths.
% Other platforms: standard Track 1 recipe.
    sep = repmat("=", 1, 62);
    logInfo("%s", sep);
    if emk.util.isOnline()
        logInfo("Recipe: pdbfixer  (MATLAB Online -- pip --user)");
        logInfo("%s", sep);
        logInfo("Description  : PDB structure preparation -- missing residues,");
        logInfo("               hydrogens, protonation state correction.");
        logInfo("               Auto-installs openmm >= 8.2 (~70 MB) as dependency.");
        logInfo("Install via  : Option A (recommended) -- via main_emk.m:");
        logInfo("               1. Open main_emk.m");
        logInfo("               2. In Section 0a, set:");
        logInfo("                    cfg.optionalLibraries.pdbfixer = true");
        logInfo("               3. Run Section 0b");
        logInfo("               Option B -- manual pip:");
        logInfo("               !~/.local/bin/pip install --user pdbfixer");
        logInfo("License      : pdbfixer: MIT  /  openmm: MIT + LGPL");
        logInfo("Notes        : openmm LGPL dynamic import is safe for MIT projects.");
    elseif ispc()
        logInfo("Recipe: pdbfixer  (Windows Desktop -- NOT SUPPORTED)");
        logInfo("%s", sep);
        logInfo("Description  : PDB structure preparation (missing residues, hydrogens).");
        logInfo("Status       : BLOCKED -- pdbfixer requires openmm, whose native .pyd");
        logInfo("               extension is blocked by Windows Smart App Control (SAC).");
        logInfo("               This is a Windows security policy and cannot be");
        logInfo("               bypassed in code.");
        logInfo("Workaround   : Use MATLAB Online (Linux) instead:");
        logInfo("               1. Open main_emk.m");
        logInfo("               2. In Section 0a, set:");
        logInfo("                    cfg.optionalLibraries.pdbfixer = true");
        logInfo("               3. Run Section 0b");
        logInfo("               For the full docking pipeline, also enable:");
        logInfo("                    cfg.optionalLibraries.meeko = true");
        logInfo("                    cfg.optionalLibraries.vina  = true");
        logInfo("License      : pdbfixer: MIT  /  openmm: MIT + LGPL");
    else
        logInfo("Recipe: pdbfixer  (Track 1 -- Embedded Python)");
        logInfo("%s", sep);
        logInfo("Description  : PDB structure preparation (missing residues, hydrogens).");
        logInfo("               Auto-installs openmm >= 8.2 (~70 MB) as dependency.");
        logInfo("Install via  : emk.setup.installExtra(""pdbfixer"")");
        logInfo("License      : pdbfixer: MIT  /  openmm: MIT + LGPL");
        logInfo("Notes        : openmm LGPL dynamic import is safe for MIT projects.");
    end
    logInfo("%s", sep);
end
